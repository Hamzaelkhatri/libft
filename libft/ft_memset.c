#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*str;

	i = 0;
	str = s;
	while (0 < n)
	{
		str[i] = c;
		i++;
		n--;
	}
	return (str);
}
